/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
type TreeNode struct {
	Val   int
	Left  *TreeNode
	Right *TreeNode
}

func postorderTraversal(root *TreeNode) []int {
	ans := make([]int, 0)
	dfs(root, &ans)
	return ans
}

func dfs(node *TreeNode, list *[]int) {
	if node == nil {
		return
	}
	if node.Left != nil {
		dfs(node.Left, list)
	}
	if node.Right != nil {
		dfs(node.Right, list)
	}
	*list = append(*list, node.Val)
}

