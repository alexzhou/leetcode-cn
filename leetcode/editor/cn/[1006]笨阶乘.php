class Solution {

    /**
     * @param Integer $N
     * @return Integer
     */
    function clumsy($N) {
        if($N==1)return 1;
        $i = 0;
        $list = [$N];
        $N-=1;
        while($N>0){
            if($i%4==0){
                $list[count($list)-1] = $list[count($list)-1]*$N;
            }else if($i%4==1){
                $list[count($list)-1] = $list[count($list)-1]>0?floor($list[count($list)-1]/$N):ceil($list[count($list)-1]/$N);
            }else if($i%4==2){
                $list[] = $N;
            }else{
                $list[] = -$N;
            }
            $i++;
            $N--;
            //print_r($list);
        }
        return array_sum($list);
    }
}
