func maxAscendingSum(nums []int) int {
	length := len(nums)
	if length == 1 {
		return nums[0]
	}
	left := 0
	sum := nums[left]
	ret := 0
	for i := 1; i < length; i++ {
		if nums[i] > nums[left] {
			sum += nums[i]
		} else {
			ret = max(ret, sum)
			sum = nums[i]
		}
		left = i
	}

	return max(ret, sum)

}

func max(a int, b int) int {
	if a > b {
		return a
	}
	return b
}

