class Solution {

    /**
     * @param Integer[] $nums
     * @param Integer $k
     * @return Boolean
     */
    function checkSubarraySum($nums, $k) {
        $n = count($nums);
        if($n<2){
            return false;
        }
        $sum[0] = $nums[0];
        for($i=1;$i<$n;$i++){
            $sum[$i] = $sum[$i-1]+$nums[$i];
        }
        //åŒä½™å®šç†
        $map = [0=>-1];
        foreach ($sum as $i=>$value){
            $v = $value%$k;
            if(isset($map[$v])){
                if($i-$map[$v] >1){
                    return true;
                }
            }else{
                $map[$v] = $i;
            }
        }
        return false;
    }
}
