class Solution {
public:
    bool isValid(string s) {
        stack<char> cs;
        int n = s.length();
        for (int i = 0; i < n; ++i) {
            if(s.at(i)==']'){
                if(s.back()=='['){
                    s.pop_back();
                }
            }else if(s.at(i)==')'){
                if(s.back()=='('){
                    s.pop_back();
                }
            }else if (s.at(i)=='}'){
                if(s.back()=='{'){
                    s.pop_back();
                }
            }else{
                s.push_back(s.at(i));
            }

        }
        return cs.empty();
    }
};
