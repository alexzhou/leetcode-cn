#include <cstdio>
#include <vector>
#include <algorithm>
#include <iostream>
#include <stack>
using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    bool find132pattern(vector<int>& nums) {
        int n = nums.size();
        int leftMin[n];
        leftMin[0] = nums[0];
        for (int i = 1; i < n; ++i) {
            leftMin[i] = min(leftMin[i-1],nums[i-1]);
        }
        stack<int> ss;
        ss.push(nums[n-1]);
        for (int j = n-2; j >=0 ; --j) {
            int val = INT32_MIN;
            while (!ss.empty() && ss.top() < nums[j]){
               val = ss.top();
               ss.pop();
            }
            if (leftMin[j] < val){
                return true;
            }
            ss.push(nums[j]);
        }
        return false;
    }
};
int main(){
   Solution s;
   vector<int> nums{-1,3,2,0};
   cout<< s.find132pattern(nums)<<endl;
   return 0;
}
