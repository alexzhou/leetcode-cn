class Solution {
public:
    int robotSim(vector<int>& commands, vector<vector<int>>& obstacles) {
        int direx[] = {0,1,0,-1};
        int direy[] = {1,0,-1,0};
        int curx=0,cury=0;
        int curdire = 0;
        int comLen = commands.size();
        int ans = 0;
        set<pair<int, int>> obstacleSet;
        for(int i=0;i<obstacles.size();i++)
            obstacleSet.insert(make_pair(obstacles[i][0], obstacles[i][1]));

        for(int i=0;i<comLen;i++){
            if(commands[i] == -1){  // -1ï¼šå‘å³è½¬ 90 åº¦
                curdire = (curdire + 1) % 4;
            }else if(commands[i] == -2){  // -2ï¼šå‘å·¦è½¬ 90 åº¦
                curdire = (curdire + 3) % 4;
            }else{  //  1 <= x <= 9ï¼šå‘å‰ç§»åŠ¨ x ä¸ªå•ä½é•¿åº¦
                for(int j=0;j<commands[i];j++){
                    //è¯•å›¾èµ°å‡ºä¸€æ­¥ï¼Œå¹¶åˆ¤æ–­æ˜¯å¦é‡åˆ°äº†éšœç¢ç‰©ï¼Œ
                    int nx = curx + direx[curdire];
                    int ny = cury + direy[curdire];
                    //å½“å‰åæ ‡ä¸æ˜¯éšœç¢ç‚¹ï¼Œè®¡ç®—å¹¶ä¸å­˜å‚¨çš„æœ€å¤§æ¬§å¼è·ç¦»çš„å¹³æ–¹åšæ¯”è¾ƒ
                    if (obstacleSet.find(make_pair(nx, ny)) == obstacleSet.end()) {
                        curx = nx;
                        cury = ny;
                        ans = max(ans, curx*curx + cury*cury);
                    }else{
                        //æ˜¯éšœç¢ç‚¹ï¼Œè¢«æŒ¡ä½äº†ï¼Œåœç•™ï¼Œåªèƒ½ç­‰å¾…ä¸‹ä¸€ä¸ªæŒ‡ä»¤ï¼Œé‚£å¯ä»¥è·³å‡ºå½“å‰æŒ‡ä»¤äº†
                        break;
                    }
                }
            }
        }
        return ans;
    }
};
