class Solution {

    /**
     * @param Integer[] $nums
     * @return Integer
     */
    function findMin($nums) {

        $n = count($nums);
        $l = 0;
        $r = $n-1;
        if ($n==1)return $nums[0];
        while ($nums[$l]==$nums[$r] && $l<$r){
            $l++;
        }

        while($l<$r){
            $pivot = $l+floor(($r-$l)/2);
            if($nums[$pivot] <= $nums[$r]){
                $r = $pivot;
            }else{
                $l = $pivot+1;
            }
        }
        return $nums[$r];
    }
}
